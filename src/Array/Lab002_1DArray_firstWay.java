package Array;

public class Lab002_1DArray_firstWay {
    public static void main(String[] args) {
        int[] ages = new int [3];
        String[] name = new String [3];
        float[] weight = new float[3];

        ages[0]= 34;
        ages[1]= 18;
        ages[2]= 56;
        System.out.println("age is :"+ages[0]);

        name[0]= "Shadab";
        name[1]= "Rasool";
        name[2]= "Shaikh";
        System.out.println("name is :"+name[2]);

        weight[0]= 21.3f;
        weight[1]= 34.5f;
        weight[2]= 43.9f;
        System.out.println("weight is :"+weight[2]);

    }
}
